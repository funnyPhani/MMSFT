import os
import requests
import fasttext
from tqdm import tqdm

LANGID_URL = "https://dl.fbaipublicfiles.com/fasttext/supervised-models/lid.176.bin"

def download(url, fname):
    resp = requests.get(url, stream=True)
    total = int(resp.headers.get('content-length', 0))
    with open(fname + "_part", 'wb') as file, tqdm(
        desc="Downloading langid model",
        total=total,
        unit='iB',
        unit_scale=True,
        unit_divisor=1024,
    ) as bar:
        for data in resp.iter_content(chunk_size=1024):
            size = file.write(data)
            bar.update(size)
    
    os.rename(fname + "_part", fname)

def load_langid_model(cache_dir=None):
    if not cache_dir:
        try:
            import torch
            from torch.hub import _get_torch_home
            torch_cache_home = _get_torch_home()
        except ImportError:
            torch_cache_home = os.path.expanduser(
                os.getenv(
                    'TORCH_HOME', 
                    os.path.join(os.getenv('XDG_CACHE_HOME', '~/.cache'), 'torch')
                )
            )

        cache_dir = os.path.join(torch_cache_home, 'fasttext')

    model_path = os.path.join(cache_dir, "lid.176.bin")
    if not os.path.isfile(model_path):
        os.makedirs(os.path.dirname(model_path), exist_ok=True)
        download(LANGID_URL, model_path)

    return fasttext.load_model(model_path)


